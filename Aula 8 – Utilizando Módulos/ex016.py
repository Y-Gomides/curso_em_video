# Exercício 16 – Quebrando um número

"""
Crie um programa que leia um número Real qualquer pelo
teclado e mostre na tela a sua porção Inteira.
"""

numeral = float(input("Digite um númeral com decimais: "))
inteiro = int(numeral)
print("O numeral {} tem o valor {} em sua porção inteira.".format(numeral, inteiro))
